int	ft_sqrt(int nb)
{
	int	k;
	int	l;

	k = 0;
	l = nb;
	while (k >= 0)
	{
		if (l == nb)
		{
			if (l == k * k)
			{
				return (k);
			}
		}
		else
			return (0);
		k++;
	}
	return (0);
}
