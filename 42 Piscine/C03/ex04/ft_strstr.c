char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (to_find[j] == '\0')
		return (str);
	while (str[i] != '\0')
	{
		while (str[i + j] != '\0' && str[i + j] == to_find[j])
		{
			j++;
			if (to_find[j] == '\0')
				return (str + i);
		}
		i++;
		j = 0;
	}
	return (0);
}

#include <stdio.h>
int main()
{

    char str[]="ali tarla top at";
    char to_find[]="t";

    printf( "sonuc = %s " , ft_strstr(str , to_find ) );
    return(0);
}