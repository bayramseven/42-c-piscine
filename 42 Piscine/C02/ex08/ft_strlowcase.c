char	*ft_strlowcase(char *str)
{
	int	u;

	u = 0;
	while (str[u] != '\0')
	{
		if (str[u] >= 'A' && str[u] <= 'Z')
			str[u] += 'a' - 'A';
		u++;
	}
	return (str);
}
